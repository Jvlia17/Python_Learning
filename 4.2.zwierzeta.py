# 4.2 Zwierzeta. Spróbuj znaleźć trzy różne zwierzęta charakteryzujące się podobnymi cechami. Umieść ich nazwy na liście, a następnie wyświetl je za pomocą pętli for.

zwierzeta = ["lis", "wilk", "pies"]

for zwierze in zwierzeta:
    print(f"{zwierze.title()} może wydawać się uroczy, ale lepiej go nie denerwować")
print("\nWszystkie wymienione powyżej zwierzęta są wspaniałe!")